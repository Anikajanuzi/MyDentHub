function getLanguage() {
  return localStorage.getItem(languageStorageKey) || "en";
}

function t(key) {
  const language = getLanguage();
  return translations[language]?.[key] || translations.en[key] || key;
}

function applyTranslations(root = document) {
  const language = getLanguage();
  document.documentElement.lang = language === "sq" ? "sq" : "en";

  root.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(element.dataset.i18n);
  });

  root.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    element.placeholder = t(element.dataset.i18nPlaceholder);
  });

  root.querySelectorAll("[data-language]").forEach((button) => {
    button.classList.toggle("active", button.dataset.language === language);
  });
}

function setLanguage(language) {
  localStorage.setItem(languageStorageKey, translations[language] ? language : "en");
  applyTranslations();
  window.dispatchEvent(new CustomEvent("mydenthub:languagechange", { detail: { language: getLanguage() } }));
}

function bindLanguageControls(root = document) {
  root.querySelectorAll("[data-language]").forEach((button) => {
    button.addEventListener("click", () => setLanguage(button.dataset.language));
  });
  applyTranslations(root);
}

window.MyDentHubI18n = {
  applyTranslations,
  bindLanguageControls,
  getLanguage,
  setLanguage,
  t,
};
